<h3>F.A.Q.s</h3>
<p><b> What are gamebooks ?</b></p>
<p><b>Answer:</b>Gamebooks are books where you choose the path you take by navigating to the numbered paragraph that corresponds with your choice.</p>
<p><b>Where does the story take place?</b></p>
<p><b>Answer:</b>The story can take place in many places. It can be in space, in a haunted house, or in a deep forest. It all depends on the author''s choice of location and genre.</p>
<p><b>What is the objective of the gamebooks?</b></p>
<p><b>Answer:</b>The objective of gamebooks is to finish the adventure successfully, without dying by making a wrong decision or being killed in combat.</p>
<p><b>When I encounter a creature what should i do?</b></p>
<p><b>Answer:</b>Unless explicitly stated in the text that you can escape a battle, you often have no choice but to fight any monsters you meet. If you have higher Skill score than your enemy, you should win fights more easily.</p>
<h4>Can I save my progress in the adventure?</h4>
<p><b>Answer:</b>Of course you can save, but I decided to add the saving feature to Quest for the Ebony Wand,Curse of Blackwood Manor and Legacy of the Vampire only because they are longer adventures than the others on this site.</p>
